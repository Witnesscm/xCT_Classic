	local name = GetSpellInfo(spell)
	if name then
		spellLookupLocalized[name] = icon
	end
end


function lib:GetSpellID(spellName, unit, auraType)

	if spellName == MELEE then
		return 6603

	elseif auraType and unit then
		if auraType == AURA_TYPE_DEBUFF then
			return select(10, AuraUtil.FindAuraByName(spellName, unit, "HARMFUL")) or 0
		else
			return select(10, AuraUtil.FindAuraByName(spellName, unit)) or 0
		end

	else

		local spellID = spellLookupLocalized[spellName]
		if not spellID then
			spellID = spellNameToID[spellName]
		end
	
		return spellID or 0
	end
end

local prevID
local counter = 0
local function processSpellsTable()
    counter = 0
    local index, id = next(SpellsTable, prevID)
    while (id and counter < 150) do
	
	    if not spellNameToID[GetSpellInfo(id)] then
            spellNameToID[GetSpellInfo(id)] = id
		end

        counter = counter + 1
        prevID = index
        index, id = next(SpellsTable, prevID)
    end
    if (id) then
        C_Timer.After(1, processSpellsTable)
    else
	    --print("end")
	end
end
lib.SpellsTimer = C_Timer.NewTimer(15, processSpellsTable)


